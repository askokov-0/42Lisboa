#include <stdio.h>

int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	while (i != nb)
	{
		if (nb == 1 || nb <= 0)
			return (0);
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
