#include <unistd.h>

void	ft_fw(char *str)
{
	int	i;

	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	while (!(((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')))
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char *argv[])
{
	if (argc == 2)
	{
		ft_fw(argv[1]);
	}
	write(1, "\n", 1);
}
