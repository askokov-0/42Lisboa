#include "libft.h"

int	ft_memcmp(const void *str1, const void *str2, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		if (str1 > str2)
			return (1);
		else if (str1 < str2)
			return (-1);
		i++;
	}
	return (0);
}